/**
 * Koordinaten für eine PLZ ermitteln.
 * 1. Exakter Match
 * 2. Prefix-Match mit 3 Ziffern
 * 3. Prefix-Match mit 2 Ziffern (Region)
 * 4. null wenn kein Match
 */
export function getCoordinatesForPLZ(plz: string): Coordinates | null {
  if (!plz || typeof plz !== "string") return null;

  const cleaned = plz.trim().replace(/\s/g, "");

  // 1. Exakter Match
  if (PLZ_COORDINATES[cleaned]) {
    return PLZ_COORDINATES[cleaned];
  }

  // 2. Prefix-Match mit 4 Ziffern (für 5-stellige PLZ)
  if (cleaned.length === 5) {
    const prefix4 = cleaned.slice(0, 4);
    if (PLZ_COORDINATES[prefix4]) {
      return PLZ_COORDINATES[prefix4];
    }
  }

  // 3. Prefix-Match mit 3 Ziffern
  const prefix3 = cleaned.slice(0, 3);
  const keys3 = Object.keys(PLZ_COORDINATES).filter((k) => k.startsWith(prefix3));
  if (keys3.length > 0) {
    return PLZ_COORDINATES[keys3[0]];
  }

  // 4. Prefix-Match mit 2 Ziffern (grobe Region)
  const prefix2 = cleaned.slice(0, 2);
  const keys2 = Object.keys(PLZ_COORDINATES).filter((k) => k.startsWith(prefix2));
  if (keys2.length > 0) {
    return PLZ_COORDINATES[keys2[0]];
  }

  return null;
}

/**
 * Haversine-Distanz zwischen zwei Koordinaten in Kilometern.
 */
export function haversineDistance(
  lat1: number,
  lng1: number,
  lat2: number,
  lng2: number
): number {
  const R = 6371; // Erdradius in km
  const dLat = ((lat2 - lat1) * Math.PI) / 180;
  const dLng = ((lng2 - lng1) * Math.PI) / 180;
  const a =
    Math.sin(dLat / 2) * Math.sin(dLat / 2) +
    Math.cos((lat1 * Math.PI) / 180) *
      Math.cos((lat2 * Math.PI) / 180) *
      Math.sin(dLng / 2) *
      Math.sin(dLng / 2);
  return R * 2 * Math.atan2(Math.sqrt(a), Math.sqrt(1 - a));
}
